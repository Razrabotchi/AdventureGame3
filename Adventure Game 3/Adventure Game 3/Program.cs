    internal class Player    
    {
        public int x, y;
        public char[] bag;
        char[,] map;
        public Player(int x, int y, char[] bag, char[,] map)
        {
            this.x = x;
            this.y = y;
            this.bag = bag;
            this.map = map;
        }

        public void Move() 
        {
            if (Console.KeyAvailable)
            {
                ConsoleKeyInfo userKey = Console.ReadKey();
                switch (userKey.Key)
                {
                    case ConsoleKey.UpArrow:
                        if (map[x - 1, y] != '#')
                        {
                            x--;
                        }
                        break;

                    case ConsoleKey.DownArrow:
                        if (map[x + 1, y] != '#')
                        {
                            x++;
                        }
                        break;

                    case ConsoleKey.LeftArrow:
                        if (map[x, y - 1] != '#')
                        {
                            y--;
                        }
                        break;

                    case ConsoleKey.RightArrow:
                        if (map[x, y + 1] != '#')
                        {
                            y++;
                        }
                        break;

                    case ConsoleKey.W:
                        if (map[x - 1, y] != '#')
                        {
                            x--;
                        }
                        break;

                    case ConsoleKey.S:
                        if (map[x + 1, y] != '#')
                        {
                            x++;
                        }
                        break;

                    case ConsoleKey.A:
                        if (map[x, y - 1] != '#')
                        {
                            y--;
                        }
                        break;

                    case ConsoleKey.D:
                        if (map[x, y + 1] != '#')
                        {
                            y++;
                        }
                        break;
                }
            }

            if (map[x, y] != ' ')
            {
                char[] tempBag = new char[bag.Length + 1];
                for (int i = 0; i < bag.Length; i++)
                {
                    tempBag[i] = bag[i];
                }
                tempBag[tempBag.Length - 1] = map[x, y];
                bag = tempBag;
                map[x, y] = ' ';
            }
        }
    }

